export function TechIcon({ slug, size = 16 }: { slug?: string; size?: number }) {
  const Icon = slug ? icons[slug] : undefined;
  if (!Icon) {
    return (
      <span className="text-cyan-400" style={{ fontSize: size - 2 }} aria-hidden>
        ✦
      </span>
    );
  }
  return <Icon size={size} color="default" />;
}
